for line in lines:
    parts = line.split('\t')
    if len(parts) != 2:
        continue
    last_name, phone = parts
    customer_id = str(uuid.uuid4())
    # Prefix phone with +91 if it's 10 digits
    if len(phone) == 10 and phone.isdigit():
        phone = f"+91{phone}"
    
    sql = f"INSERT INTO customers (id, last_name, phone_number, created_at, updated_at) VALUES ('{customer_id}', '{last_name}', '{phone}', '{now}', '{now}') ON CONFLICT (phone_number) DO NOTHING;"
    sql_statements.append(sql)

